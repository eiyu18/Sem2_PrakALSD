public class StudentList {
    String name;
    int NIM;
    int Year;
    int midTermScore;
    int finalScore;

    public StudentList(String name, int NIM, int Year, int midTermScore, int finalScore) {
        this.name = name;
        this.NIM = NIM;
        this.Year = Year;
        this.midTermScore = midTermScore;
        this.finalScore = finalScore;
    }
}
